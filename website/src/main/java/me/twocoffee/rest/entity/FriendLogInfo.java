package me.twocoffee.rest.entity;

import java.util.Date;

public class FriendLogInfo {
	private SimpleAccountInfo from;
	
	private SimpleAccountInfo to;
	
	private Date date;

	public SimpleAccountInfo getFrom() {
		return from;
	}

	public void setFrom(SimpleAccountInfo from) {
		this.from = from;
	}

	public SimpleAccountInfo getTo() {
		return to;
	}

	public void setTo(SimpleAccountInfo to) {
		this.to = to;
	}

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}
}
