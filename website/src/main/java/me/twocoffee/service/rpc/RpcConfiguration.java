package me.twocoffee.service.rpc;

public class RpcConfiguration {
	private String host;
	private boolean DBresource;

	public String getHost() {
		return host;
	}

	public void setHost(String host) {
		this.host = host;
	}

	public boolean isDBresource() {
		return DBresource;
	}

	public void setDBresource(boolean dBresource) {
		DBresource = dBresource;
	}

	
	
	
}
